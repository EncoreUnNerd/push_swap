#include "libft.h"

int	ft_isalnum(int l)
{
	int	verif1;

	verif1 = (l >= 'a' && l <= 'z') || (l >= 'A' && l <= 'Z');
	return (verif1 || (l >= '0' && l <= '9'));
}
